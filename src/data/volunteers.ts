import type { Volunteer, JourneyStage } from '../types';
import { STAGE_ORDER } from '../types';

const today = new Date('2026-05-15');

function daysAgo(days: number): string {
  const d = new Date(today);
  d.setDate(d.getDate() - days);
  return d.toISOString().split('T')[0];
}

function buildHistory(stages: JourneyStage[], startDay: number): { stage: JourneyStage; date: string; note?: string }[] {
  return stages.map((stage, i) => ({
    stage,
    date: daysAgo(startDay - i * 3),
    note: i === 0 ? 'Cadastro inicial via formulário de interesse' : undefined,
  }));
}

function stagesUpTo(target: JourneyStage): JourneyStage[] {
  const idx = STAGE_ORDER.indexOf(target);
  return STAGE_ORDER.slice(0, idx + 1);
}

